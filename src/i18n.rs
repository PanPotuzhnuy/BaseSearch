//! Interface strings: English by default, with translations available.

#[derive(Clone, Copy, PartialEq, Eq, Debug, Default)]
pub enum Lang {
    #[default]
    En,
    Ua,
    Ru,
    De,
    Es,
    Fr,
    Pl,
    Pt,
    Ro,
    Hu,
    Bg,
    Be,
    Zh,
}

impl Lang {
    pub fn code(self) -> &'static str {
        match self {
            Lang::En => "en",
            Lang::Ua => "ua",
            Lang::Ru => "ru",
            Lang::De => "de",
            Lang::Es => "es",
            Lang::Fr => "fr",
            Lang::Pl => "pl",
            Lang::Pt => "pt",
            Lang::Ro => "ro",
            Lang::Hu => "hu",
            Lang::Bg => "bg",
            Lang::Be => "be",
            Lang::Zh => "zh",
        }
    }

    pub fn from_code(code: &str) -> Lang {
        match code {
            "ua" => Lang::Ua,
            "ru" => Lang::Ru,
            "de" => Lang::De,
            "es" => Lang::Es,
            "fr" => Lang::Fr,
            "pl" => Lang::Pl,
            "pt" => Lang::Pt,
            "ro" => Lang::Ro,
            "hu" => Lang::Hu,
            "bg" => Lang::Bg,
            "be" => Lang::Be,
            "zh" => Lang::Zh,
            _ => Lang::En,
        }
    }

    pub fn label(self) -> &'static str {
        match self {
            Lang::En => "English",
            Lang::Ua => "Українська",
            Lang::Ru => "Русский",
            Lang::De => "Deutsch",
            Lang::Es => "Español",
            Lang::Fr => "Français",
            Lang::Pl => "Polski",
            Lang::Pt => "Português",
            Lang::Ro => "Română",
            Lang::Hu => "Magyar",
            Lang::Bg => "Български",
            Lang::Be => "Беларуская",
            Lang::Zh => "中文",
        }
    }

    pub const ALL: [Lang; 13] = [
        Lang::En,
        Lang::Ua,
        Lang::Ru,
        Lang::De,
        Lang::Es,
        Lang::Fr,
        Lang::Pl,
        Lang::Pt,
        Lang::Ro,
        Lang::Hu,
        Lang::Bg,
        Lang::Be,
        Lang::Zh,
    ];
}

